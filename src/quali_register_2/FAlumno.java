    private void cmdAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAceptarActionPerformed
        // TODO add your handling code here:
        bd.actualizar("Insert into alumnos (matricula, nombre, apellido_paterno, apellido_materno, grado, grupo, carrera) values("+Double.valueOf(txtMatricula.getText())+",'"+txtNombre.getText()+"','"+txtApPat.getText()+"','"+txtApMat.getText()+"',"+Integer.valueOf(cbxGrado.getSelectedItem().toString())+",'"+txtGrupo.getText()+"',"+Integer.valueOf(cbxCarrera.getSelectedItem().toString())+")");
    }//GEN-LAST:event_cmdAceptarActionPerformed

    private void cmdValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdValidarActionPerformed
        try {
            // TODO add your handling code here:
            ResultSet r = bd.consultar("Select * from alumnos values where matricula ="+Double.valueOf(txtMatricula.getText()));
            if(r.next()){
                JOptionPane.showMessageDialog(null, "El alumno ya ha sido registrado, intente de nuevo con otra matricula");
                txtMatricula.setText("");
            }else{
                txtNombre.enable(true);
                txtApPat.enable(true);
                txtApMat.enable(true);
                cbxGrado.enable(true);
                txtGrupo.enable(true);
                cbxCarrera.enable(true);
                txtMatricula.enable(false);
                cmdValidar.enable(false);
                cmdAceptar.enable(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FAlumno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmdValidarActionPerformed

