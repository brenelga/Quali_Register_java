    private void cmdValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdValidarActionPerformed
        try {
            // TODO add your handling code here:
            ResultSet r = bd.consultar("Select * from administrador where usuario = '"+txtUs.getText()+"';");
            if (r.next()){
                JOptionPane.showMessageDialog(null, "El nombre de Usuario no existe, cree uno nuevo");
            }else{
                cmdGuardar.setEnabled(true);
            }} catch (SQLException ex) {
            Logger.getLogger(FRegAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmdValidarActionPerformed

